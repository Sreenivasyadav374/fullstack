const mongoose = require("mongoose");

const empSchema = mongoose.Schema(
  {
    userName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    refreshToken: {
      type: String,
    },
  },
  { timestamps: true }
);
const employee = mongoose.model("employee", empSchema);
module.exports = employee;
